    private static int calculateDigit6(int n) {
        int digit = (int) (Math.log10(n));
        digit = (int) Math.pow(10, digit);
        return digit;
    }

    public static int reverse6(int n) {

        if (n < 10) {
            return n;
        }
        int digit = calculateDigit6(n);
        return reverse6(n % digit) * 10 + n / digit;
    }

