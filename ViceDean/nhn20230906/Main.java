    public static int solutionFour(int a, int b) {
        int reverseA = reversFour(a);
        int reverseB = reversFour(b);

        return reversFour(reverseA + reverseB);
    }

    private static int reversFour(int a) {
        int result = 0;
        int t = 10;

        while (a != 0) {
            if (result != 0) {
                result *= t;
            }

            result += a % t;
            a = a / t;
        }

        return result;
    }

    // solution
    public static int solution5(int a, int b) {
        return reverse5(reverse5(a) + reverse5(b));
    }

    static int reverse5(int n) {
        String temp = String.valueOf(n);
        StringBuilder sb = new StringBuilder();

        for (int i = temp.length() - 1; i >= 0; i--) {
            sb.append(temp.charAt(i));
        }
        return Integer.parseInt(sb.toString());
    }


