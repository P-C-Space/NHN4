    public static int solutionTwo(int a, int b) {
        return recursionSolutionTwo(recursionSolutionTwo(a) + recursionSolutionTwo(b));
    }

    public static int recursionSolutionTwo(int a) {
        int length = (int) (Math.log10(a) + 1);

        if (a < 10) {
            return a;
        }

        int digit = a % 10;
        digit *= Math.pow(10, length - 1);
        return digit + recursionSolutionTwo(a / 10);

    }

