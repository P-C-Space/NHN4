    public static int solutionThree(int a, int b) {
        int first = 0;
        int second = 0;

        for (int i = 0; a != 0; i++) {
            first *= 10;
            first += a % 10;
            a /= 10;
        }
        for (int i = 0; b != 0; i++) {
            second *= 10;
            second += b % 10;
            b /= 10;
        }
        int result = first + second;

        int answer = 0;

        for (int i = 0; result != 0; i++) {
            answer *= 10;
            answer += result % 10;
            result /= 10;
        }
        return answer;
    }

