    public static int solution(int a, int b) {
        return recursionSolution(recursionSolution(a, 0) + recursionSolution(b, 0), 0);
    }

    public static int recursionSolution(int a, int digit) {

        if (a == 0) {
            return digit;
        }
        digit = digit * 10 + a % 10;
        return recursionSolution(a / 10, digit);

    }

